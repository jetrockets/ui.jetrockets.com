        --color-background: oklch(0.13 0.004 var(--accent-hue));
        --color-foreground: oklch(0.98 0 0);
        --color-surface: oklch(0.17 0.006 var(--accent-hue));
        --color-surface-foreground: oklch(0.98 0 0);
        --color-overlay: oklch(0 0 0 / 0.7);

