class BuilderController < ApplicationController
  layout "builder"
  DEFAULTS = {
    accent_hue: 262,
    gray_chroma: 0.006,
    radius_base: "lg",
    radius_form: "md"
  }.freeze

  RADIUS_VALUES = {
    "none" => "0",
    "sm" => "0.25rem",
    "md" => "0.375rem",
    "lg" => "0.5rem",
    "xl" => "0.75rem",
    "full" => "9999px"
  }.freeze

  def index
    @theme = DEFAULTS
  end

  def download
    theme = DEFAULTS.merge(theme_params.to_h.symbolize_keys)
    css_content = generate_theme_css(theme)

    send_data css_content,
              filename: "theme.css",
              type: "text/css",
              disposition: "attachment"
  end

  private

  def theme_params
    params.permit(:accent_hue, :gray_chroma, :radius_base, :radius_form, :theme)
  end

  def generate_theme_css(theme)
    radius_base = RADIUS_VALUES[theme[:radius_base]] || RADIUS_VALUES["lg"]
    radius_form = RADIUS_VALUES[theme[:radius_form]] || RADIUS_VALUES["md"]
    include_dark = theme[:theme] == "dark"

    css = <<~CSS
      /*
       * Theme Variables
       * Generated by Theme Builder
       */

      :root {
        --accent-hue: #{theme[:accent_hue]};
        --gray-chroma: #{theme[:gray_chroma]};
        --radius-base: #{radius_base};
        --radius-field: #{radius_form};
      }
    CSS

    css += dark_theme_css(theme) if include_dark
    css
  end

